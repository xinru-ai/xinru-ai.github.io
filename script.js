const root = document.documentElement;
const toggle = document.getElementById("langToggle");
const personaGrid = document.getElementById("personaGrid");

function getInitialLanguage() {
  const saved = localStorage.getItem("xinru-site-lang");
  if (saved === "zh" || saved === "en") return saved;
  return navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

function renderPersonas(lang) {
  personaGrid.innerHTML = personas
    .map((persona) => {
      const name = lang === "zh" ? persona.zhName : persona.enName;
      const altName = lang === "zh" ? persona.enName : persona.zhName;
      const desc = lang === "zh" ? persona.zhDesc : persona.enDesc;
      return `
        <article class="persona-card" style="--persona-color: ${persona.color}">
          <div class="persona-head">
            <div>
              <div class="persona-name">${name}</div>
              <div class="persona-en">${altName}</div>
            </div>
            <span class="persona-mode">${persona.mode}</span>
          </div>
          <p>${desc}</p>
        </article>
      `;
    })
    .join("");
}

function applyLanguage(lang) {
  const strings = content[lang];
  root.lang = lang === "zh" ? "zh-CN" : "en";
  localStorage.setItem("xinru-site-lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const path = node.dataset.i18n.split(".");
    const value = path.reduce((acc, key) => acc?.[key], strings);
    if (typeof value === "string") {
      node.textContent = value;
    }
  });

  document.title = lang === "zh" ? "心如 Xinru" : "Xinru";
  toggle.textContent = lang === "zh" ? "中文 / EN" : "EN / 中文";
  renderPersonas(lang);
}

let currentLanguage = getInitialLanguage();
applyLanguage(currentLanguage);

toggle.addEventListener("click", () => {
  currentLanguage = currentLanguage === "zh" ? "en" : "zh";
  applyLanguage(currentLanguage);
});
